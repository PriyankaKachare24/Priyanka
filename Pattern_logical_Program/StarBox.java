package Pattern_logical_Program;

public class StarBox {

	public static void main(String[] args) {
//		*****
//		*****
//		*****
//		*****
	//Rows=4;col=5
		for(int i=1;i<=4;i++) //outer for loop for Rows
		{
		  for(int j=1;j<=5;j++) 
		  {
			 System.out.print("*");  //Inner for loop for col
		  }	
		  System.out.println();
		}
		

	}

}
